class Materia:
    """Una materia esta compuesta por el nombre, el numero de semestre y el numero de Cupos Habilitados"""
    def __init__(self,nombre,semestre,cuposHabilitados,creditos):
        self.nombre = nombre
        self.semestre = semestre
        self.cuposHabilitados = cuposHabilitados
        self.creditos = creditos





